def multi_type_decoder(obj):
    """decode the Json to python"""
    if "__NNData__" in obj:
        item = obj["__NNData__"]
        data = NNData(item["x"], item["y"], item["train_percentage"])
        data.train_indices = item["train_indices"]
        data.test_indices = item["test_indices"]
        data.train_pool = col.deque(item["train_pool"]["__deque__"])
        data.test_pool = col.deque(item["test_pool"]["__deque__"])
        return data

    return obj


class MultiTypeEncoder(json.JSONEncoder):
    """Encode python to Json"""
    def default(self, obj):
        if isinstance(obj, NNData):
            return {'__NNData__':  obj.__dict__}
        if isinstance(obj, col.deque):
            return {'__deque__':  list(obj)}

        return obj.__dict__


def show_round_trip_values(item_for_round_trip):
    print("ORIGINAL: ", item_for_round_trip)
    json_enhanced = json.dumps(item_for_round_trip,
                               cls=MultiTypeEncoder)
    print("ENHANCED JSON STRING: ", json_enhanced)
    py_enhanced = json.loads(json_enhanced, object_hook=multi_type_decoder)
    print("RECONSTITUTED PYTHON OBJECT: ", py_enhanced)
    print()


